package Loops1;

public class P2 {
    public static void main(String[] args) {
        int i,j;
        for(i = 0; i < 4; i++) {
            j = 0;
            while(j < 4) {
                System.out.print("*");
                j++;
            }
            System.out.println();
        }
    }
}
